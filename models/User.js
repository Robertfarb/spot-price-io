const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// Create User Mongo DB Schema
const UserSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  role: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

User = mongoose.model('users', UserSchema);
module.exports = User;